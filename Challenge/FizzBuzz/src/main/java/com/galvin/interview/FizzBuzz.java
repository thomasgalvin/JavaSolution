package com.galvin.interview;

/**
 * Ah, FizzBuzz. This one is a classic. It also, somehow, weeds out roughly 75%
 * of entry-level candidates. If you can pass this test, you're well on your
 * way.
 * 
 * FizzBuzz is simple. Actually simple. This isn't a trick question, we aren't 
 * looking for a cleverly elegant solution, we just want to see how you think
 * through a problem.
 * 
 * All you have to do is:
 *   * Print the numbers 1 through 100
 *   * If the number is evenly divisible by 3 and 5, print "FizzBuzz"
 *   * If the number is only evenly divisible by 3 print "Fizz"
 *   * If the number is only evenly divisible by 5 print "Buzz"
 * 
 * For example:
 * 
 * 1 
 * 2 
 * 3 Fizz
 * 4 
 * 5 Buzz
 * 6 Fizz
 * 7 
 * 8 
 * 9 Fizz
 * 10 Buzz
 * 11 
 * 12 Fizz
 * 13 
 * 14 
 * 15 FizzBuzz
 * ...
 */
public class FizzBuzz
{
    public void fizzBuzz(){
        for( int i = 1; i <= 100; i++ ){
            System.out.print( i );
            System.out.print( " " );
            
            if( i % 3 == 0 ) {
                System.out.print( "Fizz" );
            }
            
            if( i % 5 == 0 ) {
                System.out.print( "Buzz" );
            }
            
            System.out.println();
        }
    }
}
