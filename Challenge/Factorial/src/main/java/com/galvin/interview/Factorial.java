package com.galvin.interview;

/**
 * This class computes the factorial of a number: the product of all numbers
 * from 1 to n, typically represented as n!. By definition, 0! is 1.
 * 
 * For example, 5! = 5*4*3*2*1 = 120.
 */
public class Factorial
{
    public long factorial( int input ){
        long result = 1;
        
        for( int i = 1; i <= input; i++ ){
            result *= i;
        }
        
        return  result;
    }
}
